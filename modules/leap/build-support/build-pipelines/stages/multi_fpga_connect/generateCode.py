                    header.write('let send_' + dangling.name + '_decompressed <- mkDecompressor();\n')
                    header.write('Get#('+ dangling.raw_type + ') get_' + dangling.name + '_decompressed = toGet(send_' + dangling.name + '_decompressed);\n')
